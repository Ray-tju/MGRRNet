import torch.nn as nn
import torch.nn.functional as F
from im2mesh.layers import (
    ResnetBlockFC, CResnetBlockConv1d,
    CBatchNorm1d, CBatchNorm1d_legacy,
    ResnetBlockConv1d
)


class Decoder(nn.Module):
    ''' Decoder class.

    It does not perform any form of normalization.

    Args:
        dim (int): input dimension
        z_dim (int): dimension of latent code z
        c_dim (int): dimension of latent conditioned code c
        hidden_size (int): hidden size of Decoder network
        leaky (bool): whether to use leaky ReLUs
    '''

    def __init__(self, dim=3, z_dim=128, c_dim=128,
                 hidden_size=128, leaky=False):
        super().__init__()
        self.z_dim = z_dim
        self.c_dim = c_dim

        # Submodules
        self.fc_p = nn.Linear(dim, hidden_size)

        if not z_dim == 0:
            self.fc_z = nn.Linear(z_dim, hidden_size)

        if not c_dim == 0:
            self.fc_c = nn.Linear(c_dim, hidden_size)

        self.block0 = ResnetBlockFC(hidden_size)
        self.block1 = ResnetBlockFC(hidden_size)
        self.block2 = ResnetBlockFC(hidden_size)
        self.block3 = ResnetBlockFC(hidden_size)
        self.block4 = ResnetBlockFC(hidden_size)

        self.fc_out = nn.Linear(hidden_size, 1)

        if not leaky:
            self.actvn = F.relu
        else:
            self.actvn = lambda x: F.leaky_relu(x, 0.2)

    def forward(self, p, z, c=None, **kwargs):
        batch_size, T, D = p.size()

        net = self.fc_p(p)

        if self.z_dim != 0:
            net_z = self.fc_z(z).unsqueeze(1)
            net = net + net_z

        if self.c_dim != 0:
            net_c = self.fc_c(c).unsqueeze(1)
            net = net + net_c

        net = self.block0(net)
        net = self.block1(net)
        net = self.block2(net)
        net = self.block3(net)
        net = self.block4(net)

        out = self.fc_out(self.actvn(net))
        out = out.squeeze(-1)

        return out


class DecoderCBatchNorm(nn.Module):
    ''' Decoder with conditional batch normalization (CBN) class.

    Args:
        dim (int): input dimension
        z_dim (int): dimension of latent code z
        c_dim (int): dimension of latent conditioned code c
        hidden_size (int): hidden size of Decoder network
        leaky (bool): whether to use leaky ReLUs
        legacy (bool): whether to use the legacy structure
    '''

    def __init__(self, dim=3, z_dim=128, c_dim=128,
                 hidden_size=256, leaky=False, legacy=False):
        super().__init__()
        self.z_dim = z_dim
        if not z_dim == 0:
            self.fc_z = nn.Linear(z_dim, hidden_size)

        self.fc_p = nn.Conv1d(dim, hidden_size, 1)
        self.block0 = CResnetBlockConv1d(c_dim, hidden_size, legacy=legacy)
        self.block1 = CResnetBlockConv1d(c_dim, hidden_size, legacy=legacy)
        self.block2 = CResnetBlockConv1d(c_dim, hidden_size, legacy=legacy)
        self.block3 = CResnetBlockConv1d(c_dim, hidden_size, legacy=legacy)
        self.block4 = CResnetBlockConv1d(c_dim, hidden_size, legacy=legacy)

        self.xfg1_decoder = DecoderCBatchNorm2(c_dim=256, n_blocks=5)
        self.xfg2_decoder = DecoderCBatchNorm2(c_dim=256, n_blocks=5)
        self.x0_decoder = DecoderCBatchNorm2(c_dim=256, n_blocks=5)
        if not legacy:
            self.bn0 = CBatchNorm1d(c_dim, hidden_size)

        else:
            self.bn0 = CBatchNorm1d_legacy(c_dim, hidden_size)

        self.fc_out = nn.Conv1d(hidden_size, 1, 1)

        if not leaky:
            self.actvn = F.relu
        else:
            self.actvn = lambda x: F.leaky_relu(x, 0.2)

    def forward(self, p, z_xfg1, z_xfg2, z_x0, z_concate_out, xfg1, xfg2, x0, concate_out, train_step, **kwargs):
        p = p.transpose(1, 2)
        batch_size, D, T = p.size()
        net = self.fc_p(p)

        if self.z_dim != 0:
            net_z = self.fc_z(z_concate_out).unsqueeze(2)
            net = net + net_z

        net0 = self.block0(net, concate_out)
        net1 = self.block1(net0, concate_out)
        net2 = self.block2(net1, concate_out)
        net3 = self.block3(net2, concate_out)
        net4 = self.block4(net3, concate_out)
        out = self.fc_out(self.actvn(self.bn0(net4, concate_out)))
        out_concat = out.squeeze(1)
        if train_step:
            out_xfg1 = self.xfg1_decoder(p, z_xfg1, xfg1, **kwargs)

            out_xfg2 = self.xfg2_decoder(p, z_xfg2, xfg2, **kwargs)

            out = self.x0_decoder(p, z_x0, x0, **kwargs)
            return out_concat, out_xfg1, out_xfg2, out
        out_xfg1 = self.xfg1_decoder(p, z_xfg1, xfg1, **kwargs)

        out_xfg2 = self.xfg2_decoder(p, z_xfg2, xfg2, **kwargs)

        out = self.x0_decoder(p, z_x0, x0, **kwargs)
        # loss_kl_concat_xfg1 = F.kl_div(F.log_softmax(out_concat, dim=-1), F.softmax(out_xfg1, dim=-1),
        #                                reduction='none')
        # loss_kl_concat_xfg2 = F.kl_div(F.log_softmax(out_concat, dim=-1), F.softmax(out_xfg2, dim=-1),
        #                                reduction='none')
        # loss_kl_concat_out = F.kl_div(F.log_softmax(out_concat, dim=-1), F.softmax(out, dim=-1),
        #                               reduction='none')
        # # print("KL1", loss_kl_concat_out.sum(-1).mean())
