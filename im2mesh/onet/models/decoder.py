        # # print("KL3", loss_kl_concat_xfg1.sum(-1).mean())
        # print("KL", (loss_kl_concat_out.sum(-1).mean() + loss_kl_concat_xfg2.sum(-1).mean() + loss_kl_concat_xfg1.sum(
        #     -1).mean()) / 3)
